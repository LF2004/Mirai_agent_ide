export const locale = ref(LOCALES.ZH);

export function setLocale(value) {
  locale.value = value === LOCALES.EN ? LOCALES.EN : LOCALES.ZH;
}

export function t(key) {
  return messages[locale.value]?.[key] || messages[LOCALES.EN][key] || key;
}

export function useLocaleOptions() {
  return computed(() => [
    { id: LOCALES.ZH, label: messages[LOCALES.ZH].chinese },
    { id: LOCALES.EN, label: messages[LOCALES.EN].english }
  ]);
}
